                        <h1> Conheça sobre o projeto</h1>
                        <p>
                            O “Controle do Manejo e Monitoramento da Arborização Urbana em Salinas”, é um projeto elaborado por um grupo de alunos do curso Técnico de Informática- Campus Salinas. O projeto teve início com um pedido de Alunos do curso superior de Engenharia Florestal-Campus
                            Salinas, para que fizesse um site no qual permita fazer o cadastro das árvores de salinas, e assim foi feito.
                        </p>
                        <p>
                            O objetivo geral do site é auxiliar no controle das árvores em Salinas, fazendo o cadastro e disponibilizando as informações necessárias e específicas das mesmas. O intuito é agilizar o cadastro e o processamento do dados, para que assim pessoas responsáveis
                            pelo projeto possam ter uma maior eficácia no monitoramento das árvores. Ainda assim tendo objetivos mais específicos como, Identificar árvores que precisam de cuidados como árvores em estados de calamidade, árvores que precisam
                            de podas, testemunhar a flora arbórea em Salinas e fazer o levantamento de espécies abundantes e escassas em Salinas.
